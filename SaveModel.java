package com.java.csv;

public class SaveModel {
	InsertModel insertModel;
	UpdateModel updateModel;
	SelectModel selectModel;

	public InsertModel getInsertModel() {
		return insertModel;
	}

	public void setInsertModel(InsertModel insertModel) {
		this.insertModel = insertModel;
	}

	public UpdateModel getUpdateModel() {
		return updateModel;
	}

	public void setUpdateModel(UpdateModel updateModel) {
		this.updateModel = updateModel;
	}

	public SelectModel getSelectModel() {
		return selectModel;
	}

	public void setSelectModel(SelectModel selectModel) {
		this.selectModel = selectModel;
	}

}
